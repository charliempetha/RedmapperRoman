def timeit(func):
    def wrapper(*args, **kwargs):
        start_time = time.time()
        result = func(*args, **kwargs)
        end_time = time.time()
        total_time = end_time - start_time
        print('='*100)
        print(f"Function <<{func.__name__}>> took {total_time:.5} seconds to run.")
        print('='*100)
        return result
    return wrapper


class BaseRunner:

    def __init__(self, outBase, input_catalog_hdf5, input_specz,
                 fracdet_map, foreground_map, SPmap_path,
                 SPmap_names = ['maglim', 'fwhm', 'airmass', 'exptime', 'skybrite'],
                 seed = 42, calib_dir = None,
                 bands = ['U', 'G', 'R', 'I', 'Z', 'F106', 'F129', 'F158', 'F184'],
                 bands_inds = [0, 1, 2, 3, 4, 5, 6, 7, 8],
                 survey  = 'roman',
                 refband = 'F184',
                 n_jobs = 32, nside_split = 32,
                 Nrandoms = 1_000_000,
                 z_range  = [0.1, 0.95],
                 color_presel_thresh = [0.2]*9,
                 config_override = None,
                 zeropoint = 22.5):
        
        """
        Initialize the Redmapper pipeline

        Parameters
        ----------
        outBase : str
            Base path or prefix for all output files generated by the pipeline.

        input_catalog_hdf5 : str
            Path to the input galaxy catalog in HDF5 format containing photometric
            and positional information.

        input_specz : str or array-like
            Path to, or array containing, spectroscopic redshift information for
            the input catalog.

        fracdet_map : str
            Path to the HEALPix fractional detection (coverage) map used to mask
            or weight objects based on survey completeness.

        foreground_map : str
            Path to a HEALPix map describing foreground contamination (e.g.,
            Galactic extinction or other systematics).

        SPmap_path : str
            Directory containing survey property (SP) maps.

        SPmap_names : list of str, optional
            Names of the survey property maps to load from `SPmap_path`. These
            typically correspond to observing conditions such as magnitude limit,
            seeing (FWHM), airmass, exposure time, and sky brightness.
            Default is ['maglim', 'fwhm', 'airmass', 'exptime', 'skybrite'].

        seed : int, optional
            Random seed used for reproducible operations such as random catalog
            generation. Default is 42.

        calib_dir : str or None, optional
            Path to calibration data used for photometric or survey calibration.
            If None, no external calibration files are used. This is useful
            only if you want to run redmapper on Dataset A using calibration
            run on dataset B. Rarely needed.

        bands : list of str, optional
            List of photometric bands present in the catalog and survey property
            maps. Default corresponds to Roman-like bands.

        bands_inds : list of int, optional
            Indices corresponding to the band ordering used internally in the
            input catalog.

        survey : str, optional
            Name of the survey configuration being simulated or processed
            (e.g., 'roman', 'lsst'). Default is 'roman'.

        refband : str, optional
            Reference band used for magnitude selections or depth cuts.
            Default is 'F184'.

        n_jobs : int, optional
            Number of parallel jobs to use for processing tasks such as map
            evaluations or catalog operations. Default is 32.

        nside_split : int, optional
            HEALPix `nside` used to split the sky into regions for parallel
            processing. Default is 32.

        Nrandoms : int, optional
            Number of random points to generate when constructing random catalogs
            for the cluster catalog. Default is 1,000,000.

        z_range : list, optional
            The minimum and maximum redshift to consider for the cluster candidate.

        color_presel_thresh: list, optional
            The threshold in, abs(mag_obs - model) < thresh, that is used to preselect
            red-ish galaxies to send to redmapper in the first place. It has dimensions
            of 1 - Nbands, and is organized as [m1 - m2, m2 - m3, ....] where mi is a
            mag in a band in the same order passed in the bands input.

        config_override: path/str, optional
            A path to the config.yaml file that tells us updated values for any
            params in the default redmapper config from Eli. If you want to update
            a value, make sure you use the same naming format as Eli's default configs:
            https://github.com/DhayaaAnbajagane/redmapper/blob/main/how-to/cal_example.yml

        zeropoint: float, optional
            The zeropoint of the fluxes in the flux-2-mag conversion. Defaults to 22.5
            based on Chun-Hao To's definitions for Roman.
        """
        
        self.outBase    = outBase
        self.seed       = seed
        self.calib_dir  = calib_dir
        self.bands      = bands
        self.bands_inds = bands_inds
        self.refband    = refband
        self.refbandind = bands_inds[bands.index(refband)]
        self.n_jobs     = n_jobs
        self.survey     = survey
        self.Nrandoms   = Nrandoms
        self.z_range    = z_range
        

        self.input_catalog_hdf5   = input_catalog_hdf5
        self.input_specz          = input_specz
        self.fracdet_map          = fracdet_map
        self.foreground_map       = foreground_map
        self.SPmap_path           = SPmap_path
        self.SPmap_names          = SPmap_names
        self.nside_split          = nside_split
        self.color_presel_thresh  = color_presel_thresh
        self.config_override      = config_override
        self.zp                   = zeropoint

        if self.calib_dir != None:
            if self.calib_dir[-1] == '/':
                self.calib_dir = self.calib_dir[:-1]
                print("Removing backspace from calib_dir")

                

