    @timeit
    def prep_training_catalog(self, n_jobs):
        
        #outpath should be whatever format is utilized in RomanCollator
        
        Collator = utils.RomanCollator(self.outBase, n_jobs = n_jobs, nside = self.nside_split, bands = self.bands)
        Collator.run(self.input_catalog_hdf5)


    @timeit
    def make_all_maps(self):

        self.make_area_mask()
        self.make_depth_map()
        self.make_depth_str_map()

    
    @timeit
    def get_fracdet_map(self):

        return hp.read_map(self.fracdet_map)
    
    @timeit
    def get_foreground_map(self):

        return hp.read_map(self.foreground_map)
    
    def get_NSIDE(self):
        
        return hp.npix2nside(self.get_fracdet_map().size)

    @timeit
    def make_area_mask(self):

        if os.path.isfile(self.outBase + '.roman_pixmask.hs'):
            print("PIX FILES ALREADY EXISTS. SKIPPING....")
            return None
        
        footprint  = self.get_fracdet_map()
        foreground = self.get_foreground_map()
        NSIDE      = hp.npix2nside(foreground.size)
        
        footprint  = footprint.astype(np.float32)
        foreground = foreground.astype(np.float32)
         

        assert footprint.size == foreground.size, f"Footprint ({footprint.size}) and Foreground ({foreground.size}) maps have different sizes"
        assert NSIDE > 32, f"You have NSIDE = {NSIDE} <= 32. We need map to be more fine-grained than the healsparse coarse gain resolution"

        print("LOADED FOOTPRINT AND FOREGROUND MASKS")

        #Zero out pixels with too low coverage
        #and pixels that are outside foreground mask
        footprint[footprint < 0.5] = hp.UNSEEN
        footprint[foreground > 0]  = hp.UNSEEN

        print("APPLY FOREGROUND MASK AND FRACGOOD > 0.5")

        hsmask    = hsp.HealSparseMap.make_empty(32, NSIDE, dtype = np.float32)
        hsmask[:] = footprint[hp.nest2ring(NSIDE, np.arange(foreground.size))].astype(np.float32)

        hsmask.write(self.outBase + '.roman_pixmask.hs', clobber = True)
        hp.write_map(self.outBase + '.roman_pixmask.hpy', hsmask.generate_healpix_map(NSIDE, nest = False), overwrite = True)

        print("WROTE TO DISK...")


    @timeit
    def make_depth_map(self, n_jobs = -1):

        self.compute_depth_pixel_process(n_jobs)
        self.prepare_sys_maps()
        self.compute_depth_map()
        self.compute_exp_limit()


    @timeit
    def prepare_sys_maps(self):

        print("CHECKING THAT MAPS EXIST")
        for b in self.bands:
            for sys in self.SPmap_names:

                path     = self.SPmap_path + "_%s_%s.hpy" % (b, sys)
                path_out = path.replace(os.path.dirname(self.SPmap_path), os.path.dirname(self.outBase))
                assert os.path.isfile(path), f"The map not found at: {path}"

                if not os.path.isfile(path_out):
                    shutil.copy(path, path_out)

        print("MAKING FRACGOOD MAPS FOR SYSMAP PROCESSING")

        path = os.path.dirname(self.outBase) + "/" +  os.path.basename(self.SPmap_path) + f'_{self.bands[-1]}_frac.hpy'
        if os.path.isfile(path):
            print("FRACMAPS EXIST. SKIPPING..")
        else:
            print("MAPS FOR FINAL BAND IS MISSING, REMAKING THEM ALL TO BE SAFE....")
            NSIDE    = self.get_NSIDE()
            FRACGOOD = hsp.HealSparseMap.read(self.outBase + '.roman_pixmask.hs')
            FRACGOOD = FRACGOOD.fracdet_map(nside=NSIDE)
            FRACGOOD = FRACGOOD.generate_healpix_map(nside = NSIDE, nest = False)
            for b in self.bands: 
                path = os.path.dirname(self.outBase) + "/" +  os.path.basename(self.SPmap_path) + f'_{b}_frac.hpy'
                print(path)
                hp.write_map(path, FRACGOOD, overwrite = True)



    @timeit
    def compute_depth_pixel_process(self, n_jobs = -1):

        filelist = sorted(glob.glob(self.outBase + '_pix*_flux.fits')) #Get all files by checking for flux fits
        filelist = [f[:-10] for f in filelist] #Remove the _flux.fits part of the filename
        filelist = [filelist[i] for i in np.random.choice(len(filelist), len(filelist), replace = False)] #Randomize to balance the load
        
        if n_jobs == -1: n_jobs = np.min([os.cpu_count(), len(filelist)])

        print(f"RUNNING {len(filelist)} PIXELS USING {n_jobs} JOBS", flush = True)

        jobs = [joblib.delayed(self._single_step_pixel_process)(f) for f in filelist]
        out  = joblib.Parallel(n_jobs = n_jobs, verbose = 10)(jobs)


    def _single_step_pixel_process(self, path):
        
        if os.path.isfile(path.replace('_flux.', '.')):
            print(f"PATH {path} EXISTS. SKIPPING PROCESS STEP....", flush = True)
        
        etools.des_depth.catalogPixelProcess(path, self.outBase, 'flux', 'flux', 'fluxerr', 1024, zp = self.zp,
                                             nSidePixFile = self.nside_split, bandList = self.bands, noGoldFlags = True, 
                                             s2nCut = 5, selectGalaxies = True, bdSizeFileType = None, nTrial = 3)



    @timeit
    def compute_depth_map(self):

        NSIDE = self.get_NSIDE()
        if os.path.isfile(self.outBase + f'_nside{NSIDE}_nest_{self.bands[-1]}_depth.fits.gz'):
            print("FILES FOUND. SKIPPING....")
            return None

        files = glob.glob(self.outBase + '_pix?????.fits')
        print(f"FOUND {len(files)} PIXEL FILES. CONSOLATING.....")
        etools.des_depth.pixelConsolidate(files, self.outBase + '_coarse_depth.fits', nSide = 1024, nest = False)
        print("FINISHED CONSOLIDATING. MAKING DEPTH MAP....")

        altbands = [self.bands[-2], self.bands[-3]] if len(self.bands) >= 3 else self.bands
        mapMaker = etools.des_depth.MakeMap(self.outBase + '_coarse_depth.fits', os.path.dirname(self.outBase),
                                            os.path.basename(self.SPmap_path) + "_%s_%s.hpy", self.outBase,
                                            maglimType = 'maglim',
                                            sysTypes = [x for x in self.SPmap_names if x != 'maglim'],
                                            bands    = self.bands,
                                            altBands = altbands,
                                            aLambda  = None,
                                            npixFit  = [1] * len(self.bands),
                                            maxTry   = 3)
        mapMaker.run()


    @timeit
    def compute_exp_limit(self):

        if os.path.isfile(self.outBase + '_coarse_depth_teff.fits'):
            print("FOUD COARSE_DEPTH_TEFF FILE. SKIPPING....")
            return None

        etools.des_depth.expLimit(self.outBase + '_coarse_depth.fits', self.bands, npixMax = [5] * len(self.bands))


    @timeit
    def make_depth_str_map(self):

        if os.path.isfile(self.outBase + f'-10{self.bands[-1]}_flux_depthstr.hs'):
            pass
        else:
            zp      = self.zp

            hsmask  = hsp.HealSparseMap.read(self.outBase + '.roman_pixmask.hs')
            
            dtype   = [('exptime', 'f4'), ('limmag', 'f4'), ('m50', 'f4'), ('fracgood', 'f4')]

            teffstr, thdr = fitsio.read(self.outBase + '_coarse_depth_teff.fits', ext = 1, trim_strings = True, header = True, lower = True)
            pivot         = thdr['PIVOT']
            NSIDE         = self.get_NSIDE()

            for b in self.bands:

                if os.path.isfile(self.outBase + f'-10{b}_flux_depthstr.hs'):
                    print("FOUND DEPTH_STR FILE", self.outBase + f'-10{b}_flux_depthstr.hs', '. SKIPPING.....')
                    continue

                
                depthfile = self.outBase + f'_nside{NSIDE}_nest_{b}_depth.fits.gz'
                depthin   = hp.read_map(depthfile, nest = True)
                depth     = hsp.HealSparseMap.make_empty(32, NSIDE, dtype=np.float32)
                depth[:]  = depthin

                depth_pix = depth.valid_pixels
                ind       = list(teffstr['band']).index(b)

                dstr = np.zeros(depth_pix.size, dtype=dtype)
                dstr['limmag']   = depth[depth_pix]
                dstr['m50']      = depth[depth_pix]
                dstr['exptime']  = np.exp(teffstr[ind]['fit'][0] + teffstr[ind]['fit'][1]*(depth[depth_pix] - pivot))
                dstr['fracgood'] = hsmask[depth_pix]

                hsdepth = hsp.HealSparseMap.make_empty(nside_coverage = 32, nside_sparse = NSIDE, dtype = dtype, primary = 'fracgood')
                use,    = np.where(dstr['fracgood'] > 0.5)
                hsdepth[depth_pix[use]] = dstr[use]

                hdr = {}
                hdr['NSIG']  = 10.0
                hdr['ZP']    = self.zp
                hdr['NBAND'] = 1
                hdr['W']     = 0.0  # wall mode
                hdr['EFF']   = 1.0

                hsdepth.metadata = hdr
                hsdepth.write(self.outBase + f'-10{b}_flux_depthstr.hs', clobber = True)

                print(f"WROTE {b}_flux_depthstr.hs TO DISK")


        
    @timeit
    def make_master_galaxy_catalog(self):


        if os.path.isfile(self.outBase + "_master_table.fit"):
            print("FOUND MASTER TABLE. SKIPPING MAKE CATALOG...")
            return None

        widebase  = self.outBase
        outbase   = self.outBase
        maskfile  = self.outBase + '.roman_pixmask.hs'
        depthfile = self.outBase + f'-10{self.refband}_flux_depthstr.hs'

        mask  = hsp.HealSparseMap.read(maskfile)
        depth = hsp.HealSparseMap.read(depthfile)

        area  = np.sum(mask.get_values_pix(mask.valid_pixels), dtype=np.float64) * hp.nside2pixarea(mask.nside_sparse, degrees = True)

        print('Area = ', area)

        limmag_max = depth.get_values_pix(depth.valid_pixels)['limmag'].max()

        print('limmag_max = ', limmag_max)

        zp       = self.zp
        errrange =[0.0, 100.0]

        # b_array  = np.array([3.27e-12, 4.83e-12, 6.0e-12, 9.0e-12])
        b_array  = np.array([1.4e-12, 9.0e-13, 1.2e-12, 1.8e-12, 7.4e-12,7.4e-12,7.4e-12,7.4e-12,7.4e-12,7.4e-12])
        bscale   = np.array(b_array) * 10.**(zp / 2.5)
        refbands = ['U', 'G', 'R', 'I', 'Z', 'Y', 'F106', 'F129', 'F158', 'F184']

        bands    = self.bands
        nmag     = len(bands)
        ref_ind  = bands.index(self.refband)

        redmapper_dtype = [ ('id', 'i8'),              # galaxy id number (unique)
                            ('ra', '>f8'),             # right ascension (degrees)
                            ('dec', '>f8'),            # declination (degrees)
                            ('refmag', '>f4'),         # total magnitude in reference band
                            ('refmag_err', '>f4'),     # error in total reference mag
                            ('mag', '>f4', nmag),      # mag array
                            ('mag_err', '>f4', nmag),  # magnitude error array
                            ('ebv', '>f4')]            # E(B-V) (systematics checking)
        info_dict = {}
        info_dict['LIM_REF'] = limmag_max
        info_dict['REF_IND'] = ref_ind
        info_dict['AREA']    = area
        info_dict['NMAG']    = nmag
        info_dict['MODE']    = self.survey.upper() # currently SDSS, DES, or LSST
        info_dict['ZP']      = zp
        info_dict['B']       = np.array([b_array[refbands.index(b.upper())] for b in self.bands]) # if magnitudes are actually luptitudes

        for b, i in zip(self.bands, self.bands_inds):
            info_dict[f'{b.upper()}_IND'] = i
    
        widefiles = sorted(glob.glob('%s_pix*_basic.fits' % (widebase)))
        maker     = redmapper.GalaxyCatalogMaker(outbase, info_dict)

        offset    = 0
        for input_file in tqdm(widefiles, desc = 'collating files'):
            
            # make all 4 mags, combining
            #   sof (wide)
            #   luptitudification

            groups = re.search('_pix(\d+)_', os.path.basename(input_file))
            pixnum = int(groups[1])

            basic  = fitsio.read(input_file, ext=1, lower=True)
            fflux  = fitsio.read(input_file.replace('_basic.fits', '_flux.fits'), ext=1, lower=True)

            galaxies         = np.zeros(basic.size, dtype = redmapper_dtype)
            galaxies['id']   = offset + np.arange(len(galaxies))
            offset          += len(galaxies)
            galaxies['ra']   = basic['ra']
            galaxies['dec']  = basic['dec']

            for i, band in enumerate(bands):

                b_i     = refbands.index(band.upper())

                flux    = fflux['flux_%s' % (band.lower())].astype(np.float64)
                fluxerr = fflux['fluxerr_%s' % (band.lower())].astype(np.float64)

                galaxies['mag'][:, i]     = 2.5 * np.log10(1.0 / b_array[b_i]) - np.arcsinh(0.5 * flux / bscale[b_i]) / (0.4 * np.log(10.0))
                galaxies['mag_err'][:, i] = 2.5 * fluxerr / (2.0 * bscale[b_i] * np.log(10.0) * np.sqrt(1.0 + (0.5 * flux / bscale[b_i])**2.))


            galaxies['refmag']     = galaxies['mag'][:, ref_ind]
            galaxies['refmag_err'] = galaxies['mag_err'][:, ref_ind]
            #galaxies['ebv'] = basic['ebv_sfd98']

            # Mark bad objects
            mark = np.ones(galaxies.size, dtype=bool)
            bad, = np.where((galaxies['refmag_err'] < errrange[0]) |
                            (galaxies['refmag_err'] > errrange[1]))
            mark[bad] = 0

            for i, band in enumerate(bands):
                bad, = np.where((galaxies['mag'][:, i] < errrange[0]) |
                                (galaxies['mag'][:, i] > errrange[1]) | 
                                (galaxies['mag_err'][:, i] < errrange[0]) |
                                (galaxies['mag_err'][:, i] > errrange[1]))
                mark[bad] = 0

            # Apply depth maps
            m50 = depth.get_values_pos(galaxies['ra'], galaxies['dec'], lonlat=True)['m50']

            gd, = np.where((mark) & (galaxies['refmag'] < m50))

            if gd.size == 0:
                print("All galaxies too faint in pixel %d" % (pixnum))
                continue

            # And append the galaxies
            maker.append_galaxies(galaxies[gd])

        maker.finalize_catalog()


    @timeit
    def make_spec_catalog_redseq(self):
        
        if os.path.isfile(self.outBase + ".train_specz.fits"):
            print("FOUND TRAIN SPECZ CATALOG. SKIPPING MAKE CATALOG...")
            return None
            
        galaxy  = redmapper.galaxy.GalaxyCatalog.from_galfile(self.outBase + "_master_table.fit")
        allspec = fitsio.read(self.input_specz)
        MODEL   = fitsio.read(os.path.dirname(__file__) + f'/../calibdata/initcolors/bc03_colors_{self.survey}.fit')

        print("LOADED ALL CATALOGS")
        
        id1, id2, dist = galaxy.match_many(allspec['ra'], allspec['dec'], 0.5/3600, maxmatch = 1)

        allspec = allspec[id1]
        galmag  = galaxy.mag[id2]

        print(f"FINISHED MATCHING. FOUND {len(allspec)} MATCHES.")

        #Now plot the sequences
        use, = np.where(galmag[:, 0] > 0.0)
        
        assert np.all(np.diff(np.array(self.bands_inds)) == 1), \
            "Input bands are offset by more than 1 filter. You need to use a new file for this analysis....Ping Dhayaa"

        QAcut  = True
        for i in range(len(self.bands) - 1):
            THRESH= self.color_presel_thresh[i]
            data  = galmag[:, i] - galmag[:, i + 1]
            model = np.interp(allspec['z'][use], MODEL['Z'], MODEL['COLOR'][:, self.bands_inds[i]])
            QAcut = QAcut & (np.abs(model - data) <= THRESH)
        
        print(f"FINISHED CULLING. WE HAVE {len(use[QAcut])} MATCHES.")

        plt.close()
        for i in range(len(self.bands) - 1):
            THRESH= self.color_presel_thresh[i]
            plt.hexbin(allspec['z'][use], (galmag[:, i] - galmag[:, i + 1])[use], extent=[0, 1, 0.0, 2.5], bins='log')
            plt.xlabel('z'); plt.ylabel(f'{self.bands[i]} - {self.bands[i + 1]}')
            plt.plot(MODEL['Z'], MODEL['COLOR'][:, self.bands_inds[i]], 'b-')
            plt.plot(MODEL['Z'], MODEL['COLOR'][:, self.bands_inds[i]] + THRESH, 'b:')
            plt.plot(MODEL['Z'], MODEL['COLOR'][:, self.bands_inds[i]] - THRESH, 'b:')
            plt.savefig(self.outBase + f"_all_specz_z_{self.bands[i]}m{self.bands[i + 1]}.png"); plt.close()

        #Save the proper sample now
        fitsio.write(self.outBase + ".train_specz.fits", allspec[use[QAcut]], clobber = True)

        print("SAVED CUT CATALOG")
    

    @timeit
    def make_calibration_hpix(self):

        specz       = fitsio.read(self.outBase + ".train_specz.fits")
        hpix_specz  = np.unique(hp.ang2pix(self.nside_split, specz['ra'], specz['dec'], lonlat = True))

        print(f"{len(hpix_specz)} PIXELS HAVE SPECZ IN THEM")

        hpix_choice = sorted(glob.glob(self.outBase + '_pix?????.fits'))
        hpix_choice = [int(h[-10:-5]) for h in hpix_choice]
        hpix_choice = [h for h in hpix_choice if h in hpix_specz]
        np.random.default_rng(seed = self.seed).shuffle(hpix_choice)
        hpix_choice = hpix_choice[:int(len(hpix_choice) * 0.5)] #Use half of sample to calibrate

        return hpix_choice
    

